            . "你必须把所有专业术语翻译成普通人听得懂的大白话。例如：" . $termExamples
            . " 记住：读者是完全不懂命理的普通人！\n"
            . $roleDescription
            . self::ALGORITHM_PRINCIPLES;
    }

    /**
     * 调用 AI API (OpenAI 协议)
     * 如果 prompt 包含命盘数据，自动在 prompt 末尾追加算法核心原则提醒
     */
    public function generate($prompt, $systemPrompt = null)
    {
        // 检测到命盘数据时，自动追加算法原则提醒
        if (strpos($prompt, '命盘') !== false || strpos($prompt, '合婚') !== false) {
            $prompt .= "\n\n【算法核心原则提醒】解读时必须严格遵循 system prompt 中的算法核心原则，按优先级顺序分析：四化 > 禄存 > 主星（必须判断庙旺/得平/落陷状态） > 辅星。成对出现的星能量远大于单个星。父母宫和疾厄宫的四化需特别警惕。疾厄宫遇天使、天刑很可能要做手术。";
        }


        $url = rtrim($this->baseUrl, '/') . '/chat/completions';

        $messages = [];
        if ($systemPrompt) {
            $messages[] = ['role' => 'system', 'content' => $systemPrompt];
        }
        $messages[] = ['role' => 'user', 'content' => $prompt];

        $data = [
            'model' => $this->model,
            'messages' => $messages,
            'temperature' => 0.7,
            'max_tokens' => 8000,
            'stream' => true // 开启流式以绕过 Nginx 超时
        ];

        // error_log('AI API Request: model=' . $this->model . ', url=' . $url);

        $ch = curl_init($url);
        curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
        curl_setopt($ch, CURLOPT_POST, true);
        curl_setopt($ch, CURLOPT_POSTFIELDS, json_encode($data));
        curl_setopt($ch, CURLOPT_HTTPHEADER, [
            'Content-Type: application/json',
            'Accept: text/event-stream',
            'Authorization: Bearer ' . $this->apiKey
        ]);
        curl_setopt($ch, CURLOPT_TIMEOUT, 600);
        curl_setopt($ch, CURLOPT_SSL_VERIFYPEER, false);
        curl_setopt($ch, CURLOPT_SSL_VERIFYHOST, 0);

        error_log("GeminiClient: Calling API [model: {$this->model}]...");
        $startTime = microtime(true);

        // Nginx 防止超时：在耗时开始前打出一大段空格强制刷新 Response Header
        // 修正：只有在非命令行模式且非 JSON 返回时才输出空格，避免损坏 AJAX 返回的 JSON 格式
        $isJson = false;
        foreach (headers_list() as $header) {
            if (stripos($header, 'Content-Type: application/json') !== false) {
                $isJson = true;
                break;
            }
        }

        if (PHP_SAPI !== 'cli' && !$isJson) {
            echo str_repeat(" ", 4096);
            @flush();
            @ob_flush();
        }

        $responseContent = '';
        $reasoningContent = '';
        $buffer = '';
        curl_setopt($ch, CURLOPT_WRITEFUNCTION, function ($curl, $data) use (&$responseContent, &$reasoningContent, &$buffer, $isJson) {
            if (PHP_SAPI !== 'cli' && !$isJson) {
                echo " "; // 大模型生成流期间不断输出空格"续命"
                @flush();
                @ob_flush();
            }

            $buffer .= $data;
            while (($pos = strpos($buffer, "\n")) !== false) {
                $line = substr($buffer, 0, $pos);
                $buffer = substr($buffer, $pos + 1);

                if (strpos($line, 'data: ') === 0) {
                    $jsonStr = substr($line, 6);
                    if (trim($jsonStr) === '[DONE]')
                        continue;
                    $chunk = json_decode($jsonStr, true);
                    
                    // 捕获 API 原生支持的思考内容 (如 DeepSeek R1)
                    if (isset($chunk['choices'][0]['delta']['reasoning_content'])) {
                        $reasoningContent .= $chunk['choices'][0]['delta']['reasoning_content'];
                    }
                    
                    if (isset($chunk['choices'][0]['delta']['content'])) {
                        $responseContent .= $chunk['choices'][0]['delta']['content'];
                    }
                }
            }
            return strlen($data);
        });

        curl_exec($ch);
        $httpCode = curl_getinfo($ch, CURLINFO_HTTP_CODE);
        $duration = round(microtime(true) - $startTime, 2);

        if (curl_errno($ch)) {
            $error = curl_error($ch);
            error_log("GeminiClient: API cURL error in {$duration}s - {$error}");
            throw new Exception('AI 服务暂时不可用，请稍后重试');
        }

        if ($httpCode !== 200) {
            error_log("GeminiClient: API failed in {$duration}s [HTTP {$httpCode}]");
            throw new Exception('AI 服务返回错误（HTTP ' . $httpCode . '），请稍后重试');
        }

        error_log("GeminiClient: API success in {$duration}s [HTTP 200]");

        // 打印思考过程到日志
        if (!empty($reasoningContent)) {
            error_log("--- AI THOUGHT (Native) ---\n" . $reasoningContent . "\n-------------------------------");
        }
        
        // 如果思考过程在正文中 (通过 <thought> 标签)
        if (preg_match('/<thought>(.*?)<\/thought>/s', $responseContent, $matches)) {
            error_log("--- AI THOUGHT (Captured) ---\n" . trim($matches[1]) . "\n---------------------------------");
            // 这里我们保留正文中的 <thought> 标签内容，以便您在日志和调试中看到，
            // 稍后在 result.php 展示给用户前可以再过滤。
        }

        if (!empty($responseContent)) {
            return $responseContent;
        }

        error_log("GeminiClient: API empty response in {$duration}s");
        throw new Exception('AI 服务返回空结果，请稍后重试');
    }


